"""
QIR (LLVM-based) Interchange Format - Complete Implementation
"""
import json
from typing import Dict, List, Optional, Any
from enum import Enum


class QIROperation(Enum):
    QUANTUM_GATE = "quantum_gate"
    MEASUREMENT = "measurement"
    ALLOCATION = "allocation"
    DEALLOCATION = "deallocation"
    CLASSICAL_COMPUTE = "classical_compute"


class QIRInstruction:
    def __init__(self, op_type, name, qubits=None, params=None, results=None, metadata=None):
        self.op_type = op_type
        self.name = name
        self.qubits = qubits or []
        self.params = params or []
        self.results = results or []
        self.metadata = metadata or {}


class QIRFunction:
    def __init__(self, name, inputs=None, instructions=None, outputs=None):
        self.name = name
        self.inputs = inputs or []
        self.instructions = instructions or []
        self.outputs = outputs or []


class QIRModule:
    def __init__(self):
        self.functions = []
        self.global_vars = {}
        self.metadata = {}
    
    def to_llvm_ir(self):
        lines = [
            "; QIR Module - LLVM-based Quantum IR",
            "; Generated by AbirQu SDK",
            "",
            "%qubit = type opaque",
            "%result = type opaque",
            "",
            "declare void @__quantum__gate_h(%qubit*)",
            "declare void @__quantum__gate_cx(%qubit*, %qubit*)",
            "declare %result* @__quantum__measure(%qubit*)",
            "",
        ]
        for func in self.functions:
            lines.append(self._function_to_ir(func))
            lines.append("")
        return "\n".join(lines)
    
    def _function_to_ir(self, func):
        lines = []
        params_str = ", ".join(["%s* %s" % (p['type'], p['name']) 
                              for p in func.inputs])
        lines.append("define void @%s(%s) {" % (func.name, params_str))
        for instr in func.instructions:
            if instr.op_type == QIROperation.QUANTUM_GATE:
                qubits_str = ", ".join(["%qubit* @q{}".format(q) for q in instr.qubits])
                lines.append("  call void @__quantum__gate_%s(%s)" % (instr.name, qubits_str))
            elif instr.op_type == QIROperation.MEASUREMENT:
                for i, q in enumerate(instr.qubits):
                    if i < len(instr.results):
                        lines.append("  %s = call %result* @__quantum__measure(%qubit* @q%d)" % 
                                       (instr.results[i], q))
        lines.append("  ret void")
        lines.append("}")
        return "\n".join(lines)
    
    def to_json(self):
        module_dict = {
            "version": "1.0",
            "metadata": self.metadata,
            "functions": []
        }
        for func in self.functions:
            func_dict = {
                "name": func.name,
                "inputs": func.inputs,
                "instructions": []
            }
            for instr in func.instructions:
                instr_dict = {
                    "op_type": instr.op_type.value,
                    "name": instr.name,
                    "qubits": instr.qubits,
                    "params": instr.params
                }
                if instr.results:
                    instr_dict["results"] = instr.results
                func_dict["instructions"].append(instr_dict)
            if func.outputs:
                func_dict["outputs"] = func.outputs
            module_dict["functions"].append(func_dict)
        return json.dumps(module_dict, indent=2)
    
    @classmethod
    def from_json(cls, json_str):
        data = json.loads(json_str)
        module = cls()
        module.metadata = data.get("metadata", {})
        for func_data in data.get("functions", []):
            func = QIRFunction(
                name=func_data["name"],
                inputs=func_data.get("inputs", []),
                outputs=func_data.get("outputs", [])
            )
            for instr_data in func_data.get("instructions", []):
                instr = QIRInstruction(
                    op_type=QIROperation(instr_data["op_type"]),
                    name=instr_data["name"],
                    qubits=instr_data.get("qubits", []),
                    params=instr_data.get("params", []),
                    results=instr_data.get("results", [])
                )
                func.instructions.append(instr)
            module.functions.append(func)
        return module


class QIRConverter:
    @staticmethod
    def from_openqasm2(qasm_str):
        from abirqu.formats.openqasm2 import parse_qasm
        circuit = parse_qasm(qasm_str)
        module = QIRModule()
        func = QIRFunction(
            name="main",
            inputs=[{"name": "q%d" % i, "type": "qubit"} 
                   for i in range(circuit.num_qubits)]
        )
        gate_map = {
            "h": "h", "x": "x", "y": "y", "z": "z",
            "cx": "cx", "cz": "cz", "s": "s", "t": "t",
            "rx": "rx", "ry": "ry", "rz": "rz"
        }
        for gate_def in circuit.gates:
            if gate_def.name == "measure":
                instr = QIRInstruction(
                    op_type=QIROperation.MEASUREMENT,
                    name="measure",
                    qubits=gate_def.qubits,
                    results=["c%d" % i for i in range(len(gate_def.classical_bits))]
                )
            else:
                qir_name = gate_map.get(gate_def.name, gate_def.name)
                instr = QIRInstruction(
                    op_type=QIROperation.QUANTUM_GATE,
                    name=qir_name,
                    qubits=gate_def.qubits,
                    params=gate_def.params
                )
            func.instructions.append(instr)
        module.functions.append(func)
        return module


def create_bell_state_qir():
    module = QIRModule()
    func = QIRFunction(
        name="main",
        inputs=[
            {"name": "q0", "type": "qubit"},
            {"name": "q1", "type": "qubit"}
        ]
    )
    func.instructions.append(QIRInstruction(
        op_type=QIROperation.QUANTUM_GATE,
        name="h",
        qubits=[0])
    )
    func.instructions.append(QIRInstruction(
        op_type=QIROperation.QUANTUM_GATE,
        name="cx",
        qubits=[0, 1])
    )
    module.functions.append(func)
    return module.to_json()


